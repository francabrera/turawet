package com.turawet.beedroid.dao;

public class Section
{
	private String	name;
	private String	id;
	
	public Section()
	{
		name = "";
		id = "";
	}
	
	public String getName()
	{
		return name;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public String getId()
	{
		return id;
	}
	
	public void setId(String id)
	{
		this.id = id;
	}
	
}
